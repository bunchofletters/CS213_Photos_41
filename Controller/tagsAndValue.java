package Controller;

public class tagsAndValue {
    
    private String tags, value;

    public tagsAndValue(String tag, String value){
        this.tags = tag;
        this.value = value;
    }

    public void setTag(String stuff){
        this.tags = value;
    }

    public void setValue(String stuff){
        this.value = stuff;
    }

    public String getTag(){
        return tags;
    }
    public String getValue(){
        return value;
    }

}
